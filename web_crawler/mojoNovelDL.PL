sub regex {
  my $content = shift;
  my @escape = qw/script div/;
  if( $content =~ s/<span itemprop="content"><div style="float: right;text-indent: 0;padding:10px;">// ){
    for my $escapeKey ( @escape ){
      $content =~ s/(<$escapeKey.+?<\/$escapeKey>)//g
    }
    $content =~ s/\<p\>\<p\>/\n/g;
    $content =~ s/\<.+?\>//g;
    $content = join "\n", map { $_ = '    '.$_; $_; } grep { $_ !~ /^$/ } map { s/^\s+//g;$_; }split '\n', $content;
    return $content;
  }
}
